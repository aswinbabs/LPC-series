#ifndef __USB_TYPE_H
#define __USB_TYPE_H

#ifndef NULL
#define NULL	((void *)0)
#endif

#ifndef FALSE
#define FALSE		(0)
#endif

#ifndef TRUE
#define TRUE		(1)
#endif

typedef unsigned int	DWORD;
typedef unsigned char	BOOL;
typedef unsigned char	BYTE;
typedef unsigned short	WORD;

#endif // __USB_TYPE_H
